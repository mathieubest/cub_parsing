#include "cub3d_mat.h"

int is_start_map(char *line)
{
	int i;

	i = 0;
	while(line[i] != '\0')
	{
		if (line[i] != ' ' && line[i] != '\t')
		{
			if (line[i] == '1')
				return (1);
			else
				return (0);
		}
		i++;
	}
	return (0);
}
